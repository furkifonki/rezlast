export const cityNames = turkeyCities.map((c) => c.name).sort((a, b) => a.localeCompare(b, 'tr'));

export function getDistrictsByCity(cityName: string): string[] {
  const city = turkeyCities.find((c) => c.name === cityName);
  return city ? [...city.districts].sort((a, b) => a.localeCompare(b, 'tr')) : [];
}
